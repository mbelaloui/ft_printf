#include "libft.h"

int		ft_atoi(const char *str)
{
	char	*s;

	s = (char *)str;
	while (*s && ((*s < 33 && *s != 27) && *s != '\200'))
		s++;
	return ((*s) ? ft_atoi_base(s, 10) : 0);
}
