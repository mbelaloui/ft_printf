#include "libft.h"

void		ft_putnbr(int nbr)
{
	if (nbr < 0)
	{
		ft_putstr("-");
		if (nbr == (-2147483648))
			ft_putstr("2147483648");
		else
			ft_putnbr(-nbr);
	}
	else
	{
		if (nbr / 10)
			ft_putnbr(nbr / 10);
		ft_putchar((nbr % 10) + '0');
	}
}
