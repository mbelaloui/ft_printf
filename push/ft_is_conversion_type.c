int		ft_is_conversion_type(char c)
{
	if (c == 's' || c == 'd' || c == 'c' || c == 'i' || c == 'o' || c == 'O'
	 || c == 'u'  || c == 'x' || c == 'X')
		return (1);
	return (0);
}
